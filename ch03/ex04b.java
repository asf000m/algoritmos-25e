public class ex04b {
    public static void main(String[] args) {
        double fahrenheit = 100.2;
        double celcius = ((fahrenheit - 32) * 5) / 9;
        System.out.println("Temperature in Fahrenheit: "+ fahrenheit +" ºF");
        System.out.println("The same temperature in Celcius: "+ celcius +" ºC");
    }
}
